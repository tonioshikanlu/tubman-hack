package androidx.renderscript;

public class Byte4 {
    public byte w;
    public byte x;
    public byte y;
    public byte z;

    public Byte4() {
    }

    public Byte4(byte b2, byte b3, byte b4, byte b5) {
        this.x = b2;
        this.y = b3;
        this.z = b4;
        this.w = b5;
    }
}
